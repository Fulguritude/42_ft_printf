#include "libft.h"

void		ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	t_u32		i;

	if (!s || !f)
		return ;
	i = 0;
	while (s[i])
	{
		(*f)(i, s + i);
		i++;
	}
}
